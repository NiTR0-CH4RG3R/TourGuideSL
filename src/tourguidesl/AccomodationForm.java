    private void button_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_updateActionPerformed
        
        try {    
            Client client = Application.g_app.GetDB().GetClientByEmail( text_field_email.getText() );

            if ( !client.GetName().equals( text_field_name.getText() ) ) {
                System.out.println( "[ERROR] : Invalid name!" );
                return;
            }

            BookingEntry entry = new BookingEntry( 
                    client.GetId(), 
                    Utility.StringToLocalDateTime(text_field_checkin.getText()),
                    Utility.StringToLocalDateTime(text_field_checkout.getText())
                    );
            
            

            Application.g_app.GetDB().UpdateBookingEntry( entry );
            ClearTextFields();
            RefreshRecordTable();
        }
        catch ( Exception e ) {
            System.out.println( "[ERROR] : " + e.getMessage() );
        }
    }//GEN-LAST:event_button_updateActionPerformed

