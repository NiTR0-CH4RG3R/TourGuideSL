    private void RefreshRecordTable() {
        try {
            DefaultTableModel table_model = ( DefaultTableModel ) table_records.getModel();
            table_model.setRowCount( 0 );

            BookingEntry[] entries = Application.g_app.GetDB().GetBookingEntries();
            for ( var entry : entries ) {
                Client client = Application.g_app.GetDB().GetClient( entry.GetClientId() );
                String data[] = { 
                    String.valueOf( entry.GetId() ), 
                    String.valueOf( client.GetId() ), 
                    client.GetName(), 
                    client.GetEmail(), 
                    Utility.LocalDateTimeToString( entry.GetCheckInTime() ),
                    Utility.LocalDateTimeToString( entry.GetCheckOutTime() )
                };
                
                table_model.addRow( data );
            }
        }
        catch ( Exception e ) {
            System.out.println( "[ERROR] : " + e.getMessage() );
        }
    }
    
    private BookingEntry GetSelectedRecord() {
        BookingEntry entry = null;
        
        try {
            int selected_row = table_records.getSelectedRow();
            int id = Integer.parseInt( table_records.getModel().getValueAt( selected_row, 0 ).toString() );
            int clientId = Integer.parseInt( table_records.getModel().getValueAt( selected_row, 1 ).toString() );
            String checkin_str = table_records.getModel().getValueAt( selected_row, 4 ).toString();
            String checkout_str = table_records.getModel().getValueAt( selected_row, 5 ).toString();

            entry = new BookingEntry( 
                    id,
                    clientId, 
                    Utility.StringToLocalDateTime(checkin_str),
                    Utility.StringToLocalDateTime(checkout_str)
            );
        }
        catch ( Exception e ) {
            System.out.println( "[ERROR] : " + e.getMessage() );
        }
        
        return entry;
    }

    private void LoadRecordsToTextFields() {
        try {
            BookingEntry selected_entry = GetSelectedRecord();
            Client selected_client = Application.g_app.GetDB().GetClient( selected_entry.GetClientId() );
            text_field_name.setText( selected_client.GetName() );
            text_field_email.setText( selected_client.GetEmail() );
            text_field_checkin.setText( Utility.LocalDateTimeToString(selected_entry.GetCheckInTime()) );
            text_field_checkout.setText( Utility.LocalDateTimeToString(selected_entry.GetCheckOutTime()) );
        }
        catch ( Exception e ) {
            System.out.println( "[ERROR] : " + e.getMessage() );
        }
    }
    
